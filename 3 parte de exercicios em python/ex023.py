import math
an=float(input('Digite um angulo '))
radiano=math.radians(an)
SENO=math.sin(radiano)
COSS=math.cos(radiano)
TANG=math.tan(radiano)
print('O angulo em SENO mede{:.2f}'.format(SENO))
print('O angulo em COSSENO mede{:.2f}'.format(COSS))
print('O angulo em TANGENTE mede {:.2f}'.format(TANG))


